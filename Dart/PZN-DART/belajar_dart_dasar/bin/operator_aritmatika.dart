void main() {
  /**
   * ? + pejumlahan
   * ? - pengurangan
   * ? * perkalian
   * ? / pembagian double
   * ? ~/ pembagian integer
   * ? % sisa pembagian
   */

  var angka1 = 10;
  var angka2 = 5;

  print('Penjumlahan: ${angka1 + angka2}');
  print('Pengurangan: ${angka1 - angka2}');
  print('Perkalian: ${angka1 * angka2}');
  print('Pembagian double: ${angka1 / angka2}');
  print('Pembagian Int: ${angka1 ~/ angka2}');
  print('Modulus: ${angka1 % angka2}');
}
