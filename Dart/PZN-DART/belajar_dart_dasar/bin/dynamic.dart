// tipe data dynamic dapat menampung semua tipe data
// tipe data dynamic bisa diubah sesuai kebutuhan

void main() {
  dynamic variable = 100; // disini tipedatanya int
  print(variable);

  variable = 'Halo'; // disini tipe data berubah menjadi string
  print(variable);

  var contoh; //ini secara otomatis akan bertipe dynamic
  contoh = 21;
  print(contoh);
}
