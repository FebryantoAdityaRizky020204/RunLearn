void main() {
  /**
   * NOTE - TERNARY OPERATOR
   * ? T.O adalah operator sederhana dari if
   * ? terdiri dari dua kondisi evaluasi, jika tru nilai pertama diambil, jika false maka nilai kedua yg diambil
   */

  var nilai = 75;
  String ucapan;

  ucapan = (nilai >= 75) ? 'Selamat Anda Lulus' : 'Maaf, Silahkan coba lagi';
  print(ucapan);
}
