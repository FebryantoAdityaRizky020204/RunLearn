void main() {
  /**
   * ? IF ELSE {else if}
   * * untuk percabangan, dimana bagan program dieksekusi jika kondisi terpenuhi
   * * -> jika tidak makan else akan dijalankan [jika ada]
   */

  var nilaiAbsen = 20;

  if (nilaiAbsen > 15) {
    print('nilai absen lebih dari 15');
  } else if (nilaiAbsen < 15) {
    print('nilai kurang dari 15');
  } else {
    print('entahlah');
  }
}
