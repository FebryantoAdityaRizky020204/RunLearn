// ? ================== Perbedaan TypeScript dengan JavaScript
/**
 * *# Fitur: [Tipe Statis ] [PEnangkapan Error] [Interoperabilitas] [OOP] [Kompilasi] [Penggunaan]
 * 
 * *# JavaScript: [tidak mendukung tipe statis] [error terdeteksi hanya saat runtime] [digunakan langsung di browser]
 * *->  [mendukung OOP, tapi tanpa dukungan eksplisit] [tidak memerlukan kompilasi] [standar untuk pengembangan WEB]
 *
 * *# TypeScript: [Mendukung tipe statis dan deklarasi tipe] [error dapat diteksi saat compile time] [harus dikompilasi ke js dulu]
 * *-> [memiliki fitur OOP seperti class, interface, dan inheritance] [harus dikompilasi ke javasccript] 
 * *-> [banyak digunakan di proyek besar atau dengan tim besar untuk manajemen kode yang lebih baik]
 * *
 */

// ? ================== Mengapa Menggunakan TypeScript
/**
 * *#Typing Statis: dengan deklarasi tipe, kesalahan terkait tipe data dapat ditemukan saat compile time, bukan runtime.
 * *-> ini membantu menghindari bug yang sulit dideteksi.
 * *#Pengembangan Berskala Besar: TypeScript sangat membantu ketika bekerja dalam tim besar karena tipe data yang eksplisit dan
 * *-> alat pengembangannya yang kuat membuat kode lebih mudah dipelihara.
 * *#Refactoring mudah: dengan fitur seperti Intellisense di editor modern, typescript memepermudah refactoring kode tanpa risiko
 * *-> merusak sistem yang ada.
 * *#Support Modern ECMAScript: typescript mendukung fitur modern ECMAScript sebelum diimplementasikan sepenuhnya di semua browser
 * *-> 
 * *
 */

