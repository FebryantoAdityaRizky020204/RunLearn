void main() {
  /**
   * NOTE Operator Perbandingan
   * ? == sama dengan
   * ? != tidak sama dengan
   * ? > lebih dari
   * ? < kurang dari
   * ? >= lebih dari atau sama dengan
   * ? <= kurang dari atau sama dengan
   */

  // NOTE : Menggunakan Operator Perbandingan
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);
  print('adit' == 'adit');
}
