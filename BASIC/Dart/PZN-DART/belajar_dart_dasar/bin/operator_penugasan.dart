void main() {
  // NOTE : operator penugasa
  /**
   * ? a+= 10 => a = a + 10
   * ? a-= 10 => a = a - 10
   * ? a*= 10 => a = a * 10
   * ? a/= 10 => a = a / 10
   * ? a~/= 10 => a = a ~/ 10
   * ? a%= 10 => a = a % 10
   */

  var a = 10;
  a += 10;
  print('a+=10 => $a');

  int b = 21;
  b *= 2;
  print('b*=2 => $b');
}
